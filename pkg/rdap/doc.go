// rdap is an HTTP and json client used for Registration Data
// Access Protocol (RDAP).
//
// RDAP aims to provide access control, authentication, authorization,
// availability, data confidentiality, and data integrity over the
// WHOIS protocol for registration authorities.
//
// This package is designed to be used as a library and offers a flexible
// interface for supplying auth and a more friendly response format than
// the raw RDAP responses.
package rdap
